public class CWH_PracticeSet_05_10 {

    public static void main(String[] args) {

        /* A do while loop is executed:
             1. At least once --> CORRECT
             2. At least twice
             3. At most once
        */
        int i = 5;
        do {
            System.out.println("Hello");
        } while(i<3);
    }
}
