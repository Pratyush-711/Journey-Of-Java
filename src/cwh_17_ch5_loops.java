public class cwh_17_ch5_loops {

    public static void main(String[] args) {
        // While Loop
        System.out.println("While Loop");
        int num = 1;
        while (num<=10) {
            System.out.println(num);
            num++;
        }
        // Quick Quiz
        System.out.println("Print natural numbers from 100 to 200");
        int n = 100;
        while (n<=200) {
            System.out.println(n);
            n++;
        }
    }
}
